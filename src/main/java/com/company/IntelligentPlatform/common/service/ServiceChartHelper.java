    public static ServiceComChartUIModel convertChartModelToUI(ServiceComChartModel charModel) {
        ServiceComChartUIModel chartUIModel = new ServiceComChartUIModel();
        List<ServiceChartDataSeries> dataSeries = charModel.getDataSeries();
        List<ServiceChartDataSeriesUIModel> dataUISeries = new ArrayList<ServiceChartDataSeriesUIModel>();
        for (ServiceChartDataSeries series : dataSeries) {
            ServiceChartDataSeriesUIModel seriesUIModel = new ServiceChartDataSeriesUIModel();
            seriesUIModel.setData(series.getValueList());
            seriesUIModel.setName(series.getObjectName());
            if (series.getChartType() != null && !series.getChartType().equals(ServiceEntityStringHelper.EMPTYSTRING)) {
                seriesUIModel.setType(series.getChartType());
            }
            dataUISeries.add(seriesUIModel);
        }
        chartUIModel.setSeries(dataUISeries);
        chartUIModel.setCategories(charModel.getCategories());
        chartUIModel.setSubTitle(charModel.getSubTitle());
        chartUIModel.setTitle(charModel.getTitle());
        chartUIModel.setyAxisTitle(charModel.getyAxisTitle());
        // Convert pie chart series UI model
        ServicePieChartDataSeriesUIModel pieChartDataSeriesUIModel = convertToPieSeries(chartUIModel);
        List<ServicePieChartDataSeriesUIModel> pieSeries = new ArrayList<ServicePieChartDataSeriesUIModel>();
        pieSeries.add(pieChartDataSeriesUIModel);
        chartUIModel.setPieSeries(pieSeries);
        return chartUIModel;
    }

    public static ServicePieChartDataSeriesUIModel convertToPieSeries(ServiceComChartUIModel chartUIModel) {
        ServicePieChartDataSeriesUIModel pieChartDataSeriesUIModel = new ServicePieChartDataSeriesUIModel();
        pieChartDataSeriesUIModel.setName(chartUIModel.getTitle());
        List<ServicePieChartDataUnion> dataUnionList = new ArrayList<ServicePieChartDataUnion>();
        List<ServiceChartDataSeriesUIModel> dataSeries = chartUIModel.getSeries();
        double grossAmount = 0;
        for (ServiceChartDataSeriesUIModel series : dataSeries) {
            ServicePieChartDataUnion dataUnion = new ServicePieChartDataUnion();
            dataUnion.setName(series.getName());
            double averageAmount = 0;
            if (series.getData() == null || series.getData().size() == 0) {
                averageAmount = 0;
            } else {
                for (double value : series.getData()) {
                    averageAmount = value + averageAmount;
                }
                averageAmount = averageAmount / (series.getData().size());
            }
            dataUnion.setY(averageAmount);
            grossAmount = grossAmount + dataUnion.getY();
            dataUnionList.add(dataUnion);
        }
        // post-process y value
        for (ServicePieChartDataUnion dataUnion : dataUnionList) {
            if (grossAmount != 0) {
                dataUnion.setY(dataUnion.getY() / grossAmount * 100);
            }
        }
        pieChartDataSeriesUIModel.setData(dataUnionList);
        return pieChartDataSeriesUIModel;
    }

    /**
     * [Internal method] Merge value to data series list by checking existence
     * of object UUID
     *
     * @param dataSeries
     * @param objectUUID
     * @param value
     */
    public void mergeDataSeries(List<ServiceChartDataSeries> dataSeries,
                                String objectUUID, Double value) {
        for (ServiceChartDataSeries serviceChartDataSeries : dataSeries) {
            if (objectUUID.equals(serviceChartDataSeries.getObjectUUID())) {
                serviceChartDataSeries.getValueList().add(value);
            }
        }
    }

