    /**
     * Generate the time slot list
     *
     * @param unit
     * @param startDate
     * @param endDate
     * @return
     */
    public static List<ServiceChartTimeSlot> genChartTimeSlots(int unit,
                                                               Date startDate, Date endDate) {
        if (unit == ServiceCalendarHelper.UNIT_MONTH) {
            return genMonthTimeSlot(startDate, endDate);
        }
        if (unit == ServiceCalendarHelper.UNIT_WEEK) {
            return genWeeklyTimeSlot(startDate, endDate);
        }
        if (unit == ServiceCalendarHelper.UNIT_DAY) {
            return genDailyTimeSlot(startDate, endDate);
        }
        return null;
    }

    protected static List<ServiceChartTimeSlot> genMonthTimeSlot(
            Date startDate, Date endDate) {
        // [Step1] calculate the size of time slot list
        List<ServiceChartTimeSlot> timeSlots = new ArrayList<ServiceChartTimeSlot>();
        Calendar startCalendar = Calendar.getInstance();
        startCalendar.setTime(startDate);
        Calendar endCalendar = Calendar.getInstance();
        endCalendar.setTime(endDate);
        int size = ServiceEntityDateHelper.getDiffMonth(startCalendar,
                endCalendar);
        Calendar tmpCalendar = startCalendar;
        // [Step2] process of generate time slot list
        for (int i = 0; i < size; i++) {
            ServiceChartTimeSlot newSlot = new ServiceChartTimeSlot();
            newSlot.setStartDate(tmpCalendar.getTime());
            int actualMonth = tmpCalendar.get(Calendar.MONTH) + 1;
            String xTSLabel = tmpCalendar.get(Calendar.YEAR) + "-"
                    + actualMonth;
            xTSLabel = xTSLabel + "(" + actualMonth + "." + tmpCalendar.get(Calendar.DAY_OF_MONTH);
            tmpCalendar.add(Calendar.MONTH, 1);
            newSlot.setEndDate(tmpCalendar.getTime());
            int actualEndMonth = tmpCalendar.get(Calendar.MONTH) + 1;
            xTSLabel = xTSLabel + "-" + actualEndMonth + "." + tmpCalendar.get(Calendar.DAY_OF_MONTH) + ")";
            newSlot.setxTimeSlotLabel(xTSLabel);
            timeSlots.add(newSlot);
        }
        // [Step3] post step: check if the last data generated by time slot
        // cover the real last date, if not, post add extra time slot
        long diff = tmpCalendar.getTime().getTime() - endDate.getTime();
        if (diff < 0) {
            ServiceChartTimeSlot newSlot = new ServiceChartTimeSlot();
            newSlot.setStartDate(tmpCalendar.getTime());
            int actualMonth = tmpCalendar.get(Calendar.MONTH) + 1;
            String xTSLabel = tmpCalendar.get(Calendar.YEAR) + "-"
                    + actualMonth;
            xTSLabel = xTSLabel + "(" + actualMonth + "." + tmpCalendar.get(Calendar.DAY_OF_MONTH);
            tmpCalendar.add(Calendar.MONTH, 1);
            newSlot.setEndDate(tmpCalendar.getTime());
            int actualEndMonth = tmpCalendar.get(Calendar.MONTH) + 1;
            xTSLabel = xTSLabel + "-" + actualEndMonth + "." + tmpCalendar.get(Calendar.DAY_OF_MONTH) + ")";
            newSlot.setxTimeSlotLabel(xTSLabel);
            timeSlots.add(newSlot);
        }
        return timeSlots;
    }

